# 1


N, A, B = 10, 5, 15  # 주어진 입력값

# 조건에 따라 결과 출력
if A < B + N: # 만약 A가 B와 N을 합한 값보다 적으면 
    result = "Bus"  # Bus를 출력한다.
elif A > B + N:    # 아니면 A가 B와 N을 합한 값보다 크면 
    result = "Subway"    # Subway 를 출력한다.
else:                   # 위에 조건을 모두 해당하지 않을경우에는
    result = "Anything"     # Anything 을 출력한다.


print(result)   # 위의 조건에 따라 결과 출력


